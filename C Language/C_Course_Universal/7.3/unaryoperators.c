//Unary Operators (Pre and Post Increment And Decrement)
#include<stdio.h>
int main()
{
int a=12;
printf(" A = %d\n",++a);  // Pre Increment  
printf(" A = %d\n",a++);  // Post Increment 
printf(" A = %d\n",a);
printf(" A = %d\n",--a);  // Pre decrement
printf(" A = %d\n",a--);  // Pre decrement 
printf(" A = %d\n",a); 
return 0;
}