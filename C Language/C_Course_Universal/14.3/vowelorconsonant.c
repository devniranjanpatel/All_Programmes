// Switch Case Staement
#include <stdio.h>
int main()
{
    char ab;
    printf("Enter a character : ");
    scanf("%c", &ab);
    switch (ab)
    {
    case 'a':

    case 'e':

    case 'i':

    case 'o':

    case 'A':

    case 'E':

    case 'u':

    case 'I':

    case 'O':

    case 'U':
        printf("Vowel");
        break;
    default:
        printf("Consonant");
        break;
    }
    return 0;
}
